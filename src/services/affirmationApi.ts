    const t = input.toLowerCase();
    if (t.includes("темнота") || t.includes("темно")) return "darkness";
    if (t.includes("парень") || t.includes("девушка") || t.includes("ссора"))
      return "relationship";
    if (t.includes("мама") && t.includes("ссора")) return "relationship";
    if (t.includes("начальник") || t.includes("кричит") || t.includes("орёт"))
      return "boss";
    if (t.includes("жуков") || t.includes("пауков") || t.includes("насеком"))
      return "insects";
    if (t.includes("болезнь") || t.includes("рак") || t.includes("врач"))
      return "health";
    if (t.includes("деньги") || t.includes("аренд") || t.includes("ипотек"))
      return "money";
    if (t.includes("сон") || t.includes("не сплю") || t.includes("заснуть"))
      return "sleep";
