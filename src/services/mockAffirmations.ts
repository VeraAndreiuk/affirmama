import type { GeneratedAffirmation } from "./affirmationApi";

/**
 * Local, rule-based generator that tries to keep
 * ALL affirmations concrete and tied to the situation.
 */
export function generatePersonalizedAffirmations(
  userInput: string
): GeneratedAffirmation {
  const lowerInput = userInput.toLowerCase();

  // Darkness / fear of dark
  if (lowerInput.includes("темнота") || lowerInput.includes("темно")) {
    return {
      intro:
        "Темнота действительно может пугать, и это нормально. Попробуем подобрать фразы, которые мягко поддержат тебя именно в этой ситуации.",
      affirmations: [
        "В темноте я в безопасности, даже если не всё вижу вокруг.",
        "Мой ночник или фонарик могут быть рядом, когда мне это нужно.",
        "Темнота — это просто отсутствие света, а не угроза для меня.",
        "Я могу спокойно дышать в темноте, считая вдохи и выдохи.",
        "С каждым днём я понемногу привыкаю к темноте и чувствую себя в ней чуть увереннее.",
        "Даже в темноте у меня есть внутренний свет, который остаётся со мной."
      ],
      topic: "Другое"
    };
  }

  // Conflict with mom / quarrel
  if (lowerInput.includes("мама") || lowerInput.includes("ссора")) {
    return {
      intro:
        "Ссоры с близкими, особенно с мамой, даются тяжело. Твои чувства в этом конфликте важны и заслуживают бережного отношения.",
      affirmations: [
        "Я могу выслушать маму и при этом не терять себя и свои границы.",
        "Наши отношения с мамой важны, и при этом у меня есть право на свои чувства.",
        "Я могу спокойно сказать: «мне больно от этих слов», когда готова говорить об этом.",
        "Мы обе можем сделать паузу в разговоре и вернуться к нему, когда станет немного спокойнее.",
        "Моя мама тоже человек и может ошибаться, и это не отменяет моей ценности.",
        "Даже после ссоры с мамой я имею право на тёплое и уважительное отношение к себе."
      ],
      topic: "Отношения"
    };
  }

  // Sleep / insomnia
  if (
    lowerInput.includes("не сплю") ||
    lowerInput.includes("сон") ||
    lowerInput.includes("заснуть") ||
    lowerInput.includes("бессон")
  ) {
    return {
      intro:
        "Проблемы со сном очень выматывают, и неудивительно, что это вызывает тревогу. Попробуем подобрать фразы именно для этого непростого вечера или ночи.",
      affirmations: [
        "Даже если сон не приходит сразу, я могу просто отдыхать и давать телу шанс расслабиться.",
        "Мне не нужно контролировать каждый вздох — я могу мягко наблюдать за дыханием и считать вдохи.",
        "Моё тело умеет засыпать, даже если разум сейчас тревожится и не замолкает.",
        "Каждый вечер я могу пробовать маленькие ритуалы, которые помогают мне чувствовать себя спокойнее перед сном.",
        "Даже если эта ночь окажется беспокойной, это не даёт окончательной оценки ни мне, ни завтрашнему дню.",
        "Я разрешаю себе быть уставшей и неидеальной, пока учусь бережнее относиться к своему сну."
      ],
      topic: "Здоровье"
    };
  }

  // Focus / concentration
  if (
    lowerInput.includes("не могу сосредоточиться") ||
    lowerInput.includes("концентраци") ||
    lowerInput.includes("сконцентрир")
  ) {
    return {
      intro:
        "Когда сложно сосредоточиться, всё может казаться хаотичным и бесконечно тяжёлым. Это частый опыт, особенно при усталости и тревоге.",
      affirmations: [
        "Мне не нужно выполнять всё идеально, я могу начать с одного маленького шага.",
        "Я могу выбрать одну самую простую задачу и уделить ей немного внимания.",
        "Я имею право делать короткие паузы, даже если дела ещё не закончены.",
        "Моё внимание может возвращаться волнами, и это не делает меня ленивой или «не такой».",
        "Я могу мягко вернуть себя к тому, что делаю, без ругани и самокритики.",
        "С каждым небольшим завершённым шагом я создаю для себя чуть больше ощущения опоры."
      ],
      topic: "Работа"
    };
  }

  // Generic but still tries to echo the situation explicitly.
  const shortInput =
    userInput.length > 120 ? `${userInput.slice(0, 120).trimEnd()}…` : userInput;

  return {
    intro:
      "Я вижу, что описанная ситуация сейчас даётся тебе нелегко. Это достаточно важно, чтобы отнестись к себе мягко и без осуждения.",
    affirmations: [
      `Я признаю, что сейчас мне тяжело именно из‑за того, что происходит: «${shortInput}».`,
      "Я могу говорить с собой так, как говорила бы с близким человеком, оказавшимся в похожей ситуации.",
      "Я не обязана сразу находить идеальное решение, я могу двигаться по этой ситуации маленькими шагами.",
      "Даже если мысли о происходящем возвращаются, это не делает меня слабой или неправильной.",
      "Я могу дать себе право на паузу, чтобы чуть-чуть отдышаться от всего, что связано с этой ситуацией.",
      "Даже сейчас во мне есть части, которые умеют заботиться обо мне, пока я прохожу через это."
    ],
    topic: "Другое"
  };
}

